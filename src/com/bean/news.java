package com.bean;

public class news 
{
	private int id;
	private String title;
	private String url;
	
	public int getId()
	{
		return id;
	}
	
	public String gettitle()
	{
		return title;
	}
	
	public String geturl()
	{
		return url;
	}
	
	public void setId(int id)
	{
		this.id = id;
	}
	
	public void settitle(String title)
	{
		this.title = title;
	}
	
	public void seturl(String url)
	{
		this.url = url;
	}
}
